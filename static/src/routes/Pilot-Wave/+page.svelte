    Pilot Wave Theory, also known as the de Broglie-Bohm theory or Bohmian mechanics, is an interpretation of quantum mechanics. It proposes that in addition to the wave function of quantum entities, an actual configuration of particles exists, even when they're unobserved. The movement over time of these particles is directed by what's known as a guiding equation.
</p>

<p>
    This theory is said to be deterministic and distinctly nonlocal, which means that the velocity of any particle depends on the configuration of all particles under consideration. This view contrasts with the standard Copenhagen interpretation of quantum mechanics, which poses that particles do not have specific locations until they are observed.
</p>

<p>
    One major pillar of pilot wave theory is the principle of quantum equilibrium, according to which configurations of particles are distributed according to the square magnitude of the wave function.
</p>

 <p>   
    Notable for this theory is it does not suffer from the "measurement problem," given that particles have specific configurations at all times. It was first developed in the 1920s by Louis de Broglie, and later rediscovered and extended by David Bohm in the 1950s. While not as widely regarded as some other interpretations of quantum mechanics, the pilot wave theory still holds its position in quantum mechanics as it offers a deterministic, non-subjective framework of the quantum domain.  
</p>
